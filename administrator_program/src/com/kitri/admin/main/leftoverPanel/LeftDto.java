package com.kitri.admin.main.leftoverPanel;

public class LeftDto {
	private String foodNum;
	private String foodName;
	private String foodTypeNum;
	private String foodleftNum;
	private String foodPrice;
	private String foodOrder;
	
	public String getFoodNum() {
		return foodNum;
	}
	public void setFoodNum(String foodNum) {
		this.foodNum = foodNum;
	}
	public String getFoodName() {
		return foodName;
	}
	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}
	public String getFoodTypeNum() {
		return foodTypeNum;
	}
	public void setFoodTypeNum(String foodTypeNum) {
		this.foodTypeNum = foodTypeNum;
	}
	public String getFoodleftNum() {
		return foodleftNum;
	}
	public void setFoodleftNum(String foodleftNum) {
		this.foodleftNum = foodleftNum;
	}
	public String getFoodPrice() {
		return foodPrice;
	}
	public void setFoodPrice(String foodPrice) {
		this.foodPrice = foodPrice;
	}
	public String getFoodOrder() {
		return foodOrder;
	}
	public void setFoodOrder(String foodOrder) {
		this.foodOrder = foodOrder;
	}
}
